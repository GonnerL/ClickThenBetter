package com.example.clickthenbetter;

public class DiseaseInfo {
    private int dissCd;
    private int cnt;
    private int risk;

    public int getDissCd() {
        return dissCd;
    }

    public void setDissCd(int dissCd) {
        this.dissCd = dissCd;
    }

    public int getCnt() {
        return cnt;
    }

    public void setCnt(int cnt) {
        this.cnt = cnt;
    }

    public int getRisk() {
        return risk;
    }

    public void setRisk(int risk) {
        this.risk = risk;
    }
}
